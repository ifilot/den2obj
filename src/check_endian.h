#ifndef _CHECK_ENDIAN_H
#define _CHECK_ENDIAN_H

static bool is_big_endian() {
    union {
        uint32_t i;
        char c[4];
    } bint = {0x01020304};

    return bint.c[0] == 1;
}

#endif // _CHECK_ENDIAN_H
